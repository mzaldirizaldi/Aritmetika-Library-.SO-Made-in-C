#include "aritmetika.h"

double tambah (double a, double b) {
	return a + b;
}

double kurang (double a, double b) {
	return a - b;
}

double kali (double a, double b) {
	return a * b;
}

double bagi (double a, double b) {
	return a / b;
}

int bagi_integer (int a, int b) {
	return a / b;
}

int sisabagi (int a, int b) {
	return a % b;
}
